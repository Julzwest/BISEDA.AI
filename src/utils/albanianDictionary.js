// Helper functions
export const getRandomWord = (category) => {
  const words = albanianDictionary[category] || [];
  return words[Math.floor(Math.random() * words.length)];
};

export const getRandomWords = (category, count = 1) => {
  const words = albanianDictionary[category] || [];
  const shuffled = [...words].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const containsWord = (text, category) => {
  const words = albanianDictionary[category] || [];
  const lowerText = text.toLowerCase();
  return words.some(word => lowerText.includes(word.toLowerCase()));
};

export const getAllWords = () => {
  return Object.values(albanianDictionary).flat();
};

export const getWordCount = () => {
  return getAllWords().length;
};


