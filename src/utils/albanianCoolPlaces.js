// Get cool place by city and category
export function getCoolPlace(city, category) {
  const cityData = albanianCoolPlaces[city.toLowerCase()];
  if (!cityData) return null;
  
  const places = cityData[category];
  if (!places || places.length === 0) return null;
  
  return places[Math.floor(Math.random() * places.length)];
}

// Get all cool places for a city
export function getCoolPlacesForCity(city) {
  return albanianCoolPlaces[city.toLowerCase()] || null;
}

